    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        int selectedRow = tblVerTranferencias.getSelectedRow();
        if (selectedRow != -1) {

            String idCliente = tblVerTranferencias.getValueAt(selectedRow, 0).toString();
            int confirm = JOptionPane.showConfirmDialog(this, "¿Esta seguro de borrar el Empleado:"+idCliente+"?se borraran todos los datos relacionados", "Confirmar Borrado", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {

                try (Connection conn = DBConnection.getConnection()) {
                    String sql = "DELETE FROM EMPLEADO WHERE IDEMPLEADO = ?";
                    PreparedStatement pstmt = conn.prepareStatement(sql);
                    pstmt.setString(1, idCliente);
                    int rowsAffected = pstmt.executeUpdate();
                    if (rowsAffected > 0) {
                        ((DefaultTableModel) tblVerTranferencias.getModel()).removeRow(selectedRow);
                        JOptionPane.showMessageDialog(this, "El empleado se borró exitosamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(this, "Error al borrar el Empleado.", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                    pstmt.close();
                } catch (SQLException e) {
                    e.printStackTrace();
                    JOptionPane.showMessageDialog(this, "Error al intentar borrar el empleado.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Por favor, selecciones un cliente para borrar.", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

