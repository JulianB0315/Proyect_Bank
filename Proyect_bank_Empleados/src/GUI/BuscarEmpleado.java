    private void btnCuentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCuentaActionPerformed
        DefaultTableModel model = (DefaultTableModel) tblVerTranferencias.getModel();
        model.setRowCount(0);
        String idEmpleadoBuscar = txtIEmpleado.getText();

        try (Connection conn = DBConnection.getConnection()) {
            String sql = "SELECT * FROM EMPLEADO WHERE IDEMPLEADO LIKE ?";
            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setString(1,idEmpleadoBuscar+"%");
            ResultSet rs = pstmt.executeQuery();

            if (!rs.isBeforeFirst()) {
                JOptionPane.showMessageDialog(this, "No se encontraron datos", "Sin resultados", JOptionPane.INFORMATION_MESSAGE);
                return;
            }

            while (rs.next()) {
                String idEmpleadoBase = rs.getString("IDEMPLEADO");
                String nombres = rs.getString("NOMBRE");
                String apellido = rs.getString("APELLIDO");
                String sexo = rs.getString("SEXO");
                String fechaNacimiento = rs.getString("FECHANACIMIENTO");
                String cargo = rs.getString("CARGO");
                String ContratoFecha = rs.getString("FECHACONTRATACION");
                String salario = rs.getString("SALARIO");
                String contraseña = rs.getString("CONTRASEÑA");
                model.addRow(new Object[]{idEmpleadoBase, nombres, apellido, sexo, fechaNacimiento,cargo,ContratoFecha,salario,contraseña});
            }

            rs.close();
            pstmt.close();
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al recuperar los datos de clientes.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCuentaActionPerformed

